    {
        array[i] = rand() % 1000;
        cout << i << " элемент массива = " << array[i] << endl;
    }
    cout << "Введите номер начального элемента диапазона: " << endl;
    cin >> start;//ввод значения начала диапазона
    cout << "Введите номер конечного элемента диапазона: " << endl;
    cin >> end;//ввод значения окончания диапазона
    if (end < start || start < 0 || end > 9) {
        cout << "Начальный элемент диапазона должен быть < конечного элемента диапазона и > -1! Конечный элемент диапазона должен быть < 10!" << endl;
        exit;
    }
    else {
    //вызываем функцию, вычисляющую сумму элементов массива в заданном диапазоне и выводим значение функции в консоль
    cout << "Сумма элементов массива в заданном диапазоне = " << sumRange(array, &start, &end) << endl;
}
    system("pause");
