#include <iostream>
using namespace std;

int sumRange(int array[], int* start, int* end)//создаем функцию, вычисляющую сумму элементов в заданном диапазоне
{
    int sum = 0;
    int s = *start; //начало диапазона
    int e = *end; //окончание диапазона
    for (s; s < e + 1; s++) //считаем сумму через цикл
    {
    sum = sum + array[s];
}
    return sum;//возвращаем значение суммы
}
